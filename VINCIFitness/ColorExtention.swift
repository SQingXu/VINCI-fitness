import Foundation
import UIKit

extension UIColor{
    class func vinciRed() -> UIColor{
        return UIColor(red: 239/255, green: 73/255, blue: 76/255, alpha: 1)
    }
    class func vinciBlue() -> UIColor{
        return UIColor(red: 87/255, green: 125/255, blue: 162/255, alpha: 1)
    }
    class func vinciGrey() -> UIColor{
        return UIColor(red: 199/255, green: 199/255, blue: 205/255, alpha: 1)
    }
}